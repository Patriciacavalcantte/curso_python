"""
OPERADORES DE COMPARAÇÃO (RELACIONAIS)
OP......significado exemplo (True)
>.......maior.....2 > 1
>=...... maior ou igual 2 >= 2
<....... menor..........1 < 2
==...... igual .........'a' == 'a'
!=...... diferente...... 'a' != 'b'

"""
maior = 2 > 10
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
print(diferente, maior, maior_ou_igual, menor_ou_igual, igual, diferente)